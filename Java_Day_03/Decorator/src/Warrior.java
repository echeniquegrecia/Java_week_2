public abstract class Warrior {

    private int hp;
    private int dmg;


    public int getHtp() {
        return hp;
    }

    public void setHp(int htp) {
        this.hp = hp;
    }

    public int getDmg() {
        return dmg;
    }

    public void setDmg(int dmg) {
        this.dmg = dmg;
    }

}
